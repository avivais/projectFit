<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
AChecker is used to evaluate HTML content for accessibility problems by entering the location of a web page, uploading an html file, or by pasting the complete HTML source code from a Web page.<br /><br />

AChecker produces a report of all accessibility problems for your selected guidelines. AChecker identifies 3 types of problems:<br />
<ol><li><strong>Known Problems</strong>: These are problems that have been identified with certainty as accessibility barriers. You must modify your page to fix these problems;</li><br /><li><strong>Likely Problems</strong>: These are problems that have been identified as probable barriers, but require a human to make a decision. You will likely need to modify your page to fix these problems;</li><br /><li><strong>Potential Problems</strong>: These are problems that AChecker cannot identify, that require a human decision. You may have to modify your page for these problems, but in many cases you will just need to confirm that the problem described is not present.</li></ol>

