You can use the shortcode [break] to insert a linebreak into your content.<br /><br />
This is extremely useful for spacing paragraphs, titles, headers, charts, or whatever else when switching between the Visual and the HTML editors.  It will also save properly when saving your content.<br /><br />
This has been the best workaround I can find to the tinymce stripping away br tags.

