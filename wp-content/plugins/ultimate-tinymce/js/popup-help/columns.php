<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Columns Shortcodes:</strong></span>
<br />
This option will enable the columns shortcodes usage within the post/page content area.

<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
You can use the column shortcodes to insert columns in your post/page content area.<br /><br />
For example: If you'd like to divide your content into two columns you would type:<br />
<b>[one_half]</b>This is the left column.<b>[/one_half][one_half_last]</b>This is the right column.<b>[/one_half_last]</b>.<br /><br />
Remember to "close" your short code, just as you would with HTML. Also; did you notice the <b>_last</b> added to the last content column? That's REALLY important as it will remove the margin on the right side. <b>If you forget it, your columns won't work.</b><br /><br />
How about another example?<br />
<b>[two_third]</b>This area is two thirds wide.<b>[/two_third][one_third_last]</b>This is some more content, only one third wide.<b>[/one_third_last]</b><br /><br />
You can combine the available sizes any way you'd like, but you must ensure the total width is equal to "1".<br />
For example, you can have [one_sixth]Some Content Here.[/one_sixth][one_half]Some Content Here.[/one_half][one_third_last]Some Content Here.[/one_third_last]... because the total is equal to "1".

