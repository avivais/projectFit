<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Layers Box:</strong></span>
<br />
This option will add layers functionality to the editor.  Layers can be used to add aditional content and move it around the editor window for precision placement.

<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
Layers can be used to add content and move it around the editor window for placement.  Any type of content accepted by the editor can be used in each layer.  Layers can be positioned absolutely by dragging, and can also have the z-index toggled forward and backward.<br /><br />
