<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Google Maps Box:</strong></span>
<br />
This option will enable the Google Maps Box.  This button will be added to row three of the visual editor.

<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
Placing a check in the box will enable the button in your visual editor.  Unchecking the box removes the button from the editor.
<br /><br />
Google Maps is a handy tool, used for a variety of purposes.  Here are a few examples I have seen using Google Maps:
<ul><li>Show where your next event will be held.</li><li>Show your vacation destinations.</li><li>Track your "road trip" stops along the way.</li></ul>
This plugin will add a button to row 3 of your visual editor.  When clicking the button, a popup will appear giving you a few options:
<ul><li>Address: Enter a specific address, city, state, or country.  This plugin even has auto-fill.  This box will self-populate as you type your address.</li><li>Coordinates: The coordinates will populate once you select your address.  Alternatively, if you know your coordinates, you may enter them here.</li><li>Size: You may enter a custom width and height here.</li><li>General Tab: There are a few general options which are pretty self-explanatory.</li></ul> 

