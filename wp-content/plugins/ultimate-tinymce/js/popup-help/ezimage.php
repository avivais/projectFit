<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
This option gives users a new, graphical interface for setting image attributes.  One limitation is this plugin lacks an image browser.  An image can be inserted using a url... but there is no browse feature.  Still, this plugin does offer a new interface, for editing the attributes of images.<br /><br />

Just click on any inserted image (or use the url bar in the popup window to specify an image) and click the EZ Image button.  Configure the options in the popup window to the desired specifications.  Click "Insert"... and the changes will be made.

