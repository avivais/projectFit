<br />
This option will enable the respective options for modifying the appearance of the css used in the tinymce content editor.  If nothing else works.. frustration has taken it's toll.. then these options will help alleviate the pain.  These options generate a special CSS file used during initialization of the tinymce editor.  Each of the options include the CSS <em>!important</em> delcaration to ensure they take priority in the editor.

<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
There are a few notes to be aware of:<br />
<ul>
<li>
Note 1: This option ONLY affects the tinymce content editor.  It makes NO changes whatsoever to the styles on the front end of the website.
</li><li>
Note 2: It is most likely a manual refresh of the browsers cache will need to be performed before changes may be seen.  This option can usually be found in the browsers "File", "Settings" menu.  Use Google, if necessary, to find out how to manually empty the browser cache.  If changes are still not observed, close the browser completely and try again.</ul>

