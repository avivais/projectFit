<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
Placing a check in the box will enable the button in your visual editor.  Unchecking the box removes the button from the editor.<br /><br />
The Equations plugin will add a third-party popup window which allows selection of hundreds of mathematical signs to insert into the content area.<br /><br />
The popup window is used to "build" the mathematical function.  Then, when <strong>"Copy to Document"</strong> is pressed, a graphic is "snapped" of the function and inserted into the content area.<br /><br />
Ther are literally hundreds of possibilities with this plugin; which I'll attemp to list a few here:<br />

<ul>
<li>
Functions for basic math such as addition, subtraction, multiplication and division.
</li>
<li>
Advanced functions for trigonometry, calculus, algebra, geometry, and linear differential equations.
</li>
<li>
Work with fractions, powers, variables, greek symbols, and more.
</li>
</ul>

