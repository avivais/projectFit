<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Disable wpautop:</strong></span>
<br />
This option will disable the wpautop feature in the editor.

<br /><br />

<span class="title" style="padding:0px 5px 0px 5px;background-color:#CCFCD9;border:1px solid #339966;"><strong>Usage:</strong></span>
<br />
<b>Please understand what disabling wpautop() means.</b><br /><br />

Firstly, you will need to manually add < p > and < br / > tags whenever you write a Post or Page from now on and, secondly and more importantly, you will need to go back into every existing Post and Page and manually add the < p > and < br / > tags.<br /><br />

This is because the < p > and < br / > tags in your Posts and Pages are NOT saved in the database. The < p > and < br / > tags you currently see, when viewing Posts and Pages in the browser, are added by the wpautop() function between database and browser.
<br /><br />

If you enable this option, and are not pleased with the results; you can always come back and disable this feature, restoring your original content.<br /><br />

